package com.suresh.general;

public class Increment {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int x=0;
		int y=1;
		System.out.println(x);
		System.out.println(++x);
		System.out.println(++x);
		System.out.println(x);

		System.out.println(y);
		System.out.println(y++);
		System.out.println(y);
		System.out.println(++y);

	}

}
